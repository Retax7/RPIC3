            <strong>¿Por qué participar?<br /><br /></strong>
La RPIC estudiantil es una instancia para que los estudiantes avanzados de grado o recién graduados puedan:
• Presentar y discutir sus trabajos con otros estudiantes o con especialistas de temas afines.<br />
• Asistir a tutoriales y charlas de temas que se encuentran en la frontera de cada especialidad.<br />
• Adquirir experiencia en la redacción de trabajos científicos.<br />
• Someter a evaluación de especialistas sus primeros trabajos científicos o de desarrollo.<br />
• Ganar experiencia en la defensa pública de sus trabajos.<br />
<br />
<strong>¿Quiénes pueden participar?<br /><br /></strong>
Todos los estudiantes de las carreras de ingeniería o afines (de grado y de maestría) y aquellos recientemente graduados pueden enviar sus trabajos para participar de la RPIC Estudiantil. Los trabajos serán evaluados por un Comité de Revisión y aquellos aceptados serán incluidos en las Actas de RPIC 2021 (no en IEEExplore). Pueden enviarse trabajos finales de carrera, o trabajos finales de materias, con temáticas relacionadas con el Procesamiento de Información y Control, que versen sobre temas de actualidad y que posean cierto grado de originalidad a nivel de científico o tecnológico.<br />
<br />
<strong>¿Cómo participar como autor? <br /><br /></strong>
<br />Los autores deberán enviar sus trabajos en idioma Español (o eventualmente Inglés), con un máximo de 6 páginas (tamaño A4) en formato PDF y siguiendo los lineamientos generales de RPIC (Disponibles en la sección de Autores).
<br />En el trabajo deberá proporcionar información sobre el/los autores (estudiantes y solamente un asesor/supervisor), institución universitaria, dirección postal y dirección de correo electrónico del primer autor.
<br />El trabajo debe incluir una buena definición del problema, el enfoque adoptado para su solución, conclusiones, referencias bibliográficas, y recomendaciones para trabajos futuros. Los trabajos seleccionados deberán ser expuestos por uno o más estudiantes en sesión de posters y serán evaluados por un jurado de especialistas que elegirá los mejores trabajos presentados.
<br />El poster debe tener tamaño A0(84.1cm x 118. 9cm) y se sugiere seguir el template de PowerPoint para los pósters. Se solicita a los autores de la instancia incluir la leyenda RPIC Estudiantil bajo la filiación en el trabajo presentado.

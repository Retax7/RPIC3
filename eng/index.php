<h3 class="text-celeste">Anales</h3>
<p>
Los artículos completos serán publicados en los anales de trabajos del RPIC 2021 y existe la posibilidad de que los trabajos que, a criterio del comité editorial, posean suficiente calidad sean publicados en el IEEE Xplore. Para ello, los artículos deben ser redactados en idioma inglés. Además, dado que actualmente la publicación en IEEExplorer no es gratuita los autores interesados en la publicación deberán abonar un monto que aún no está definido.
El envío de contribuciones originales puede realizarse en idioma Español o Inglés, con un máximo de 6 páginas (tamaño A4), en las áreas de interés mencionadas. El formato de los trabajos seguirá los lineamientos de IEEE, y se encuentra disponible para su descarga una plantilla para Microsoft Word y una plantilla para LATEX en la página del congreso.
